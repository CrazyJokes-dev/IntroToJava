
public class ForloopTest {

	public static void main(String[] args) {
		char firstLetter = 'a';
		char secondLetter = 'a';
		
		for(firstLetter = 'a'; firstLetter <= 'e'; firstLetter++) {
			
			
			for(secondLetter = 'a'; secondLetter <= 'e'; secondLetter++) {
				System.out.print(firstLetter);
				System.out.print(secondLetter);
				System.out.println("");
			}
			
		}
		

	}

}
